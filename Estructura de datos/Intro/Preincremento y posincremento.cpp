#include <iostream>
using namespace std;

int main()
{
    int c = 5;
    int d = 8;

    cout << c << endl;
    cout << c++ << endl;
    cout << c << endl;
    cout << ++c << endl;
    cout << c << endl;

    cout << d << endl;
    cout << d-- << endl;
    cout << d << endl;
    cout << --d << endl;
    cout << d << endl;
}
